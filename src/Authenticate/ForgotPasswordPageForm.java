    private void jButton3_resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3_resetActionPerformed
        // TODO add your handling code here:
        String email = jTextField1_email.getText();
        String newPassword = new String(jPasswordField1_new_password.getPassword());
        String confirmPassword = new String(jPasswordField2_confirm_password.getPassword());

        if (newPassword.isEmpty() || confirmPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter and confirm your new password", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!newPassword.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Main_Authenticate.resetPassword(email, newPassword);
        } 
    }//GEN-LAST:event_jButton3_resetActionPerformed


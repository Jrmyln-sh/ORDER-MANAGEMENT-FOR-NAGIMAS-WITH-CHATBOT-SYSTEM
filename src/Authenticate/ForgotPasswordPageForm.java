    private void jPasswordField1_new_passwordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField1_new_passwordFocusLost
        // TODO add your handling code here:
        char[] passwordChars = jPasswordField1_new_password.getPassword();
        String passwordText = new String(passwordChars);
        
        if(passwordText.isEmpty()) {
            jPasswordField1_new_password.setEchoChar((char)0);
            jPasswordField1_new_password.setText("New Password");
        }
    }//GEN-LAST:event_jPasswordField1_new_passwordFocusLost

    private void jPasswordField2_confirm_passwordFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField2_confirm_passwordFocusGained
        // TODO add your handling code here:
        char[] passwordChars = jPasswordField2_confirm_password.getPassword();
        String passwordText = new String(passwordChars);
        
        if(passwordText.equals("Confirm Password")) {
            jPasswordField2_confirm_password.setText("");
            jPasswordField2_confirm_password.setEchoChar('•');
        }
    }//GEN-LAST:event_jPasswordField2_confirm_passwordFocusGained

    private void jPasswordField2_confirm_passwordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordField2_confirm_passwordFocusLost
        // TODO add your handling code here:
        char[] passwordChars = jPasswordField2_confirm_password.getPassword();
        String passwordText = new String(passwordChars);
        
        if(passwordText.isEmpty()) {
            jPasswordField2_confirm_password.setEchoChar((char)0);
            jPasswordField2_confirm_password.setText("Confirm Password");
        }
    }//GEN-LAST:event_jPasswordField2_confirm_passwordFocusLost

    private void jCheckBox1_ShowPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox1_ShowPasswordActionPerformed
        // TODO add your handling code here:
        if (jCheckBox1_ShowPassword.isSelected()) {
            jPasswordField1_new_password.setEchoChar((char) 0);
            jPasswordField2_confirm_password.setEchoChar((char) 0);
        } else {
            jPasswordField1_new_password.setEchoChar('•');
            jPasswordField2_confirm_password.setEchoChar('•');
        }
    }//GEN-LAST:event_jCheckBox1_ShowPasswordActionPerformed



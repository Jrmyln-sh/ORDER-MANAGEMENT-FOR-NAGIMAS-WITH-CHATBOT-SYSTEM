    private void jButton2_verify_codeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_verify_codeActionPerformed
        // TODO add your handling code here:
        String email = jTextField1_email.getText();
        String resetCode = jTextField2_code.getText();
        if (email.isEmpty() || resetCode.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your email and reset code", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (Main_Authenticate.verifyResetCode(email, resetCode)) {
            JOptionPane.showMessageDialog(this, "Reset code verified. You can now reset your password.", "Success", JOptionPane.INFORMATION_MESSAGE);
            jPasswordField1_new_password.setEnabled(true);
            jPasswordField2_confirm_password.setEnabled(true);
            jButton3_reset.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Invalid or expired reset code", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2_verify_codeActionPerformed

